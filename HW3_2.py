#  a) 1 3 5 7 9
print(" a):", end=" ")
for i in range(1, 10, 2):
    print(i, end=" ")

#  b) 9 7 5 3 1
print("\n b):", end=" ")
for i in range(9, 0, -2):
    print(i, end=" ")

#  c) 1 8 27 64
print("\n c):", end=" ")
for i in range(1,5):
    print(i**3, end=" ")

#  d) 64 27 8 1
print("\n d):", end=" ")
for i in range (4,0,-1):
    print(i**3, end=" ")
